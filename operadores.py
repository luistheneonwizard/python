# Ejemplos de operadores aritméticos
x = 10
y = 3
print(x + y)   # 13
print(x - y)   # 7
print(x * y)   # 30
print(x / y)   # 3.333...
print(x % y)   # 1
print(x ** y)  # 1000
print(x // y)  # 3
            

# Ejemplos de operadores de comparación
a = 5
b = 10
print(a == b)   # False
print(a != b)   # True
print(a > b)    # False
print(a < b)    # True
print(a >= b)   # False
print(a <= b)   # True
            

# Ejemplos de operadores lógicos
a = True
b = False
print(a and b)  # False
print(a or b)   # True
print(not a)    # False