def saludar():
    print("¡Hola, mundo!")

# Llamar a la función

saludar()  # Imprime "¡Hola, mundo!"

def saludar(nombre):
    print(f"¡Hola, {nombre}!")

# Llamar a la función con un argumento

saludar("Luis")  # Imprime "¡Hola, Ana!"

